import requests
import pandas as pd
import time
import os

# =========================================================================
# 1. 특정 년월(1개월) 및 특정 구(LAWD_CD) 데이터를 호출하는 핵심 함수
# =========================================================================
def get_commercial_sales_data(api_key, lawd_cd, deal_ymd):
    url = "https://apis.data.go.kr/1613000/RTMSDataSvcNrgTrade/getRTMSDataSvcNrgTrade"
    params = {
        'serviceKey': api_key,          
        'LAWD_CD': str(lawd_cd),        
        'DEAL_YMD': str(deal_ymd),      
        'numOfRows': '4999',            
        'pageNo': '1',
        '_type': 'json'                 
    }

    try:
        response = requests.get(url, params=params)
        if response.status_code == 200:
            try:
                data = response.json()
            except ValueError:
                return None

            try:
                total_count = data['response']['body']['totalCount']
                if total_count == 0:
                    return pd.DataFrame()  # 거래 내역 없음

                items = data['response']['body']['items']['item']
                if isinstance(items, dict):
                    items = [items]

                return pd.DataFrame(items)
            except KeyError:
                return None
        else:
            print(f"❌ HTTP 통신 에러 [{lawd_cd}/{deal_ymd}]: {response.status_code}")
            return None
    except Exception as e:
        print(f"❌ 시스템 에러: {e}")
        return None


# =========================================================================
# 2. 특정 시·도 전체의 연단위 데이터를 수집하는 함수 (새로 변경된 핵심!)
# =========================================================================
def get_province_sales_data(api_key, province_name, start_year, end_year, district_dict):
    """
    지정한 시·도(예: 서울특별시)에 속한 모든 구의 데이터를 지정한 연도 범위 동안 수집합니다.
    """
    # 1. 입력한 시·도가 딕셔너리에 있는지 확인
    if province_name not in district_dict:
        print(f"❌ '{province_name}'은(는) 지역 코드 리스트에 없습니다. 정확한 명칭을 입력해주세요.")
        return
    
    # 해당 시·도에 속한 구 이름과 코드 목록 가져오기
    target_districts = district_dict[province_name]
    print(f"🚀 [{province_name}] 전체 데이터 수집을 시작합니다. (총 {len(target_districts)}개 시·군·구 수집 예정)")
    print(f"📅 대상 기간: {start_year}년 ~ {end_year}년\n" + "="*50)
    
    # 2. 연도별 반복
    for year in range(start_year, end_year + 1):
        province_yearly_dfs = []  # 해당 연도, 해당 시·도 전체 데이터를 모을 바구니
        print(f"\n🌟 === {year}년 [{province_name}] 수집 시작 ===")
        
        # 3. 해당 시·도에 속한 모든 시·군·구 반복
        for district_name, lawd_cd in target_districts.items():
            print(f"📍 {district_name} ({lawd_cd}) 수집 중...")
            
            # 4. 1월부터 12월까지 반복
            for month in range(1, 13):
                deal_ymd = f"{year}{month:02d}"
                
                # API 호출
                df_month = get_commercial_sales_data(api_key, lawd_cd, deal_ymd)
                
                if df_month is not None and not df_month.empty:
                    # 데이터에 어떤 구 정보인지 식별할 수 있도록 컬럼 추가해두면 편리합니다.
                    df_month['시군구명'] = district_name
                    df_month['시도명'] = province_name
                    province_yearly_dfs.append(df_month)
                    
                # API 서버 부하 및 차단 방지 (0.2초 휴식)
                time.sleep(0.2)
                
        # 5. 해당 연도의 모든 구/모든 달 데이터가 모였다면 파일 하나로 통합 저장
        if province_yearly_dfs:
            final_df = pd.concat(province_yearly_dfs, ignore_index=True)
            
            os.makedirs("province_sales_data", exist_ok=True)
            file_name = f"province_sales_data/{province_name}_실거래가_{year}.csv"
            
            final_df.to_csv(file_name, index=False, encoding='utf-8-sig')
            print(f"[{province_name}] {year}년 통합 완료! -> 총 {len(final_df)}건 저장 ({file_name})")
        else:
            print(f"⚠️ {year}년 [{province_name}]에는 수집된 데이터가 전혀 없습니다.")

    print(f"\n✨ [{province_name}]의 모든 데이터 수집이 완료되었습니다!")


